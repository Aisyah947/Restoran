    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
    if (selectedId == 0) return;

    Menu m = Menu.getById(selectedId);
    m.setNamaMenu(jTextField1.getText());
    m.setKategori(jComboBox1.getSelectedItem().toString());
    m.setHarga(Double.parseDouble(jTextField2.getText()));

    m.save();

    tampilkanData("");
    resetForm();
    selectedId = 0;
    }//GEN-LAST:event_jButton2ActionPerformed

