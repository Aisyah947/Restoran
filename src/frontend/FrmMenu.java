    private void tampilkanData(String keyword) {
    String[] kolom = {"ID", "Nama Menu", "Kategori", "Harga"};
    ArrayList<Menu> list = Menu.getAll(keyword);

    DefaultTableModel model = new DefaultTableModel(kolom, 0);

    for (Menu m : list) {
        Object[] row = {
            m.getIdMenu(),
            m.getNamaMenu(),
            m.getKategori(),
            m.getHarga()
        };
        model.addRow(row);
    }

    jTable2.setModel(model);
}
    
