    private void jTextField5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField5ActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField5.getText();
    
    String[] kolom = {"ID", "Nama", "Email", "Telepon", "Alamat"};
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(null, kolom);

    for (backend.Pelanggan p : backend.Pelanggan.cari(keyword)) {
        Object[] data = {
            p.getIdPelanggan(),
            p.getNama(),
            p.getEmail(),
            p.getTelepon(),
            p.getAlamat()
        };
        model.addRow(data);
    }

    jTable1.setModel(model);
    }//GEN-LAST:event_jTextField5ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
    if (row == -1) return;

    backend.Pelanggan p = new backend.Pelanggan();
    p.setIdPelanggan(Integer.parseInt(jTable1.getValueAt(row, 0).toString()));
    p.setNama(jTextField1.getText());
    p.setEmail(jTextField2.getText());
    p.setTelepon(jTextField3.getText());
    p.setAlamat(jTextField4.getText());

    p.update();
    loadTable();
    }//GEN-LAST:event_jButton2ActionPerformed

