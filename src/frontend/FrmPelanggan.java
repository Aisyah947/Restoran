    private void loadTable() {
    String[] kolom = {"ID", "Nama", "Email", "Telepon", "Alamat"};
    javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel(null, kolom);

    for (backend.Pelanggan p : backend.Pelanggan.getAll()) {
        Object[] data = {
            p.getIdPelanggan(),
            p.getNama(),
            p.getEmail(),
            p.getTelepon(),
            p.getAlamat()
        };
        model.addRow(data);
    }

    jTable1.setModel(model);
}
    
