    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
                try (Connection c = DBHelper.getConnection()) {
            String[] menuData = cbMenu.getSelectedItem().toString().split(" - ");
            int idMenu = Integer.parseInt(menuData[0]);

            String sql = "INSERT INTO detail_pesanan (id_pesanan, id_menu, jumlah, subtotal) VALUES (?,?,?,?)";
            PreparedStatement st = c.prepareStatement(sql);

            st.setInt(1, Integer.parseInt(cbPesanan.getSelectedItem().toString()));
            st.setInt(2, idMenu);
            st.setInt(3, Integer.parseInt(txtJumlah.getText()));
            st.setDouble(4, Double.parseDouble(txtSubtotal.getText()));

            st.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");

            loadTable();
            reset();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

