    // ==================== LOAD COMBO PESANAN ===========================
    private void loadComboPesanan() {
        try (Connection c = DBHelper.getConnection()) {
            String sql = "SELECT id_pesanan FROM pesanan";
            PreparedStatement s = c.prepareStatement(sql);
            ResultSet r = s.executeQuery();

            while (r.next()) {
                cbPesanan.addItem(r.getString("id_pesanan"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    // ==================== LOAD COMBO MENU ===========================
    private void loadComboMenu() {
        try (Connection c = DBHelper.getConnection()) {
            String sql = "SELECT id_menu, nama_menu, harga FROM menu";
            PreparedStatement s = c.prepareStatement(sql);
            ResultSet r = s.executeQuery();

            while (r.next()) {
                cbMenu.addItem(r.getInt("id_menu") + " - " + r.getString("nama_menu") + " - " + r.getDouble("harga"));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    // =================== HITUNG SUBTOTAL ==========================
    private void updateSubtotal() {
        try {
            String[] menuData = cbMenu.getSelectedItem().toString().split(" - ");
            double harga = Double.parseDouble(menuData[2]);

            int jumlah = Integer.parseInt(txtJumlah.getText());

            double total = harga * jumlah;
            txtSubtotal.setText(String.valueOf(total));

        } catch (Exception ex) {
            txtSubtotal.setText("");
        }
    }

    // =================== LOAD TABLE ===============================
    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel(
            new String[]{"ID", "Pesanan", "Menu", "Jumlah", "Subtotal"}, 0);

        try (Connection c = DBHelper.getConnection()) {
            String sql = "SELECT * FROM detail_pesanan";
            PreparedStatement st = c.prepareStatement(sql);
            ResultSet rs = st.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_detail"),
                    rs.getInt("id_pesanan"),
                    rs.getInt("id_menu"),
                    rs.getInt("jumlah"),
                    rs.getDouble("subtotal")
                });
            }

            tblDetail.setModel(model);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    // =================== RESET ===============================
private void reset() {
    // kosongkan input teks
    txtJumlah.setText("");
    txtSubtotal.setText("");

    // pilih indeks pertama kalau ada item, agar tidak melempar exception jika kosong
    if (cbPesanan.getItemCount() > 0) {
        cbPesanan.setSelectedIndex(0);
    }

    if (cbMenu.getItemCount() > 0) {
        cbMenu.setSelectedIndex(0);
    }

    // hilangkan seleksi baris di tabel
    tblDetail.clearSelection();
}


