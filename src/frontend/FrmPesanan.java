    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
    int row = jTable1.getSelectedRow();
    if (row < 0) return;

    int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());

    // Ambil ID pelanggan dari combobox
    String selected = jComboBox2.getSelectedItem().toString();
    int idPelanggan = Integer.parseInt(selected.split(" - ")[0]);

    // Buat objek pesanan
    Pesanan ps = new Pesanan();
    ps.setIdPesanan(id);
    ps.setIdPelanggan(idPelanggan);
    ps.setTanggalPesanan(jTextField2.getText());
    ps.setStatus(jComboBox1.getSelectedItem().toString());

    ps.update();
    tampilkanData();
    }//GEN-LAST:event_jButton2ActionPerformed
    
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
    int row = jTable1.getSelectedRow();

    int idPelanggan = Integer.parseInt(jTable1.getValueAt(row, 1).toString());
    String tanggal = jTable1.getValueAt(row, 2).toString();
    String status = jTable1.getValueAt(row, 3).toString();

    // Set combo berdasarkan ID
    for (int i = 0; i < jComboBox2.getItemCount(); i++) {
        String item = jComboBox2.getItemAt(i);   // contoh "1 - Budi"
        int idItem = Integer.parseInt(item.split(" - ")[0]);
        if (idItem == idPelanggan) {
            jComboBox2.setSelectedIndex(i);
            break;
        }
    }

    jTextField2.setText(tanggal);
    jComboBox1.setSelectedItem(status);
    }//GEN-LAST:event_jTable1MouseClicked

