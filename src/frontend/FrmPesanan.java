    private void tampilkanData() {
        String[] kolom = {"ID", "Pelanggan", "Tanggal", "Status"};
        ArrayList<Pesanan> list = Pesanan.getAll();

        Object[][] data = new Object[list.size()][4];
        int i = 0;
        for (Pesanan p : list) {
            data[i][0] = p.getIdPesanan();
            data[i][1] = p.getIdPelanggan();
            data[i][2] = p.getTanggalPesanan();
            data[i][3] = p.getStatus();
            i++;
        }

        DefaultTableModel model = new DefaultTableModel(data, kolom);
        jTable1.setModel(model);
    }

    // ================== COMBO PELANGGAN ==================
private void tampilkanPelanggan() {
    jComboBox2.removeAllItems();

    try (Connection conn = DBHelper.getConnection();
         Statement st = conn.createStatement();
         ResultSet rs = st.executeQuery("SELECT * FROM pelanggan")) {

        while (rs.next()) {
            int id = rs.getInt("id_pelanggan");
            String nama = rs.getString("nama");

            // Yang ditampilkan di combo = "1 - Budi"
            jComboBox2.addItem(id + " - " + nama);
        }

    } catch (SQLException e) {
        System.out.println("Error load pelanggan: " + e.getMessage());
    }
}

